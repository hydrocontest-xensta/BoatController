/* USER CODE BEGIN Header_StartControlSafran */
/**
* @brief Function implementing the controlSafran thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartControlSafran */
void StartControlSafran(void const * argument)
{
  /* USER CODE BEGIN StartControlSafran */
	float err_prec = 0;
	  /* Infinite loop */
		for(;;)
		{

			//PID
			float consigne = pulse_width[2] + MID_POS_SAFRAN; //(AMP_SAFRAN/AMP_CH3)*(pulse_width[2]-MID_POS_CH3);

			float err = consigne - increment;

			float d_err = err - err_prec;
			err_prec = err;

			float commande = 0;

			commande = - KP_PID_SAFRAN*err
					- KD_PID_SAFRAN*d_err;

			com_safran[0] = 64;
			//Choix de la direction du moteur
			if(commande > 100){
				com_safran[0] = 64 + MAX_P_SAFRAN;
				debugPrint(&huart1, com_safran);
			}
			else if(commande <= 100 && commande >STOP_VAL_SAFRAN){
				com_safran[0] = 64 + (MAX_P_SAFRAN*commande)/100;
				debugPrint(&huart1, com_safran);
			}
			else if(commande <= STOP_VAL_SAFRAN && commande >=-STOP_VAL_SAFRAN){
				com_safran[0] = 64;
				debugPrint(&huart1, com_safran);
			}
			else if(commande < -STOP_VAL_SAFRAN && commande >=-100){
				com_safran[0] = 64 + (MAX_P_SAFRAN*commande)/100;
				debugPrint(&huart1, com_safran);
			}
			else{
				com_safran[0] = 64-MAX_P_SAFRAN;
				debugPrint(&huart1, com_safran);
			}/*
			*/
			osDelay(10);
  }
  /* USER CODE END StartControlSafran */
}

